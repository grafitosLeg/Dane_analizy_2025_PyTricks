# wypisywanie elementów listy

# kod ten wypisuje na ekran każdy element listy "lista", która zawiera pieć licz.
# Moze być przydatny ddo iteracji przez elementy zboiru danych i wyświetlania ich
# jesli reszta z dzielenia (modulo) jest równa 0, to znaczy, że liczba jest parzysta

for i in range(10):
    if i % 2 == 0:
        print(i)


